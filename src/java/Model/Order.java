package Model;

import java.util.Date;

/**
 *
 * @author ngoc
 */
public class Order {
    private int orderId;
    private int userId;
    private int totalMoney;
    private Date date;
    private String status;

    public Order() {
    }

    public Order(int orderId, int userId, int totalMoney, Date date, String status) {
        this.orderId = orderId;
        this.userId = userId;
        this.totalMoney = totalMoney;
        this.date = date;
        this.status = status;
    }

    public int getOrderId() {
        return orderId;
    }

    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(int totalMoney) {
        this.totalMoney = totalMoney;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    
}
